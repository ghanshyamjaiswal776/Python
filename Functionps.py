# WAF to print the length of list .(list is the parameter)
cities = ["delhi","lucknow","nodia","gurgaon","agra","gorakhpur"]
heroes = ["thor","ranveer","sarukhkhan","salman"]

def print_len(list):
    print(len(list))
print_len(cities)
print_len(heroes)

def print_list(list):
    for item in list:
        print(item, end=" ")
print_list(heroes)

